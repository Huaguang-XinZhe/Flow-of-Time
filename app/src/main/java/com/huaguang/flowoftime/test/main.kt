fun printTrie(node: TrieNode, childPrefix: String = "") {
//    println(prefix + node.category.orEmpty()) // 这是打出类属所需要的
    val childrenEntries = node.children.entries.toList()
    println("childrenEntries = $childrenEntries")
    for ((index, entry) in childrenEntries.withIndex()) {
        val isLast = index == childrenEntries.size - 1
        if (isLast) {
            println("$childPrefix└── ${entry.key}")
            printTrie(entry.value, "$childPrefix    ")
        } else {
            println("$childPrefix├── ${entry.key}")
            printTrie(entry.value, "$childPrefix│   ")
        }
    }
}



